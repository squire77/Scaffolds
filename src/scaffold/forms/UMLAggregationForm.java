    private void handleOK() {
        if (compositeCheckBox.isSelected() != assoc.isComposite()) {            
            assoc.setComposite(compositeCheckBox.isSelected());
        }        
        
        CmAssocEnd containerEP = (link.getEndPoint1().getEPType() == UMLEndPoint.EPType.AGGREGATE)
                ? assoc.getEndPoint1() : assoc.getEndPoint2();        
        
        if (containerEP.getName() == null || !containerEP.getName().equals(containerRoleName.getText())) {
            containerEP.setName(containerRoleName.getText());
        }
        
        if (containerEP.getMultiplicity() == null || !containerEP.getMultiplicity().getExpression().equals(containerMultiplicity.getText())) {
            containerEP.getMultiplicity().setExpression(containerMultiplicity.getText());
        }                           
        
        CmAssocEnd containeeEP = (link.getEndPoint1().getEPType() != UMLEndPoint.EPType.AGGREGATE)
                ? assoc.getEndPoint1() : assoc.getEndPoint2();
        
        if (containeeEP.getName() == null || !containeeEP.getName().equals(containeeRoleName.getText())) {
            containeeEP.setName(containeeRoleName.getText());
        }
        
        if (containeeEP.getMultiplicity() == null || !containeeEP.getMultiplicity().getExpression().equals(containeeMultiplicity.getText())) {
            containeeEP.getMultiplicity().setExpression(containeeMultiplicity.getText());
        }       
        
        if (containerEP.isNavigable() != containerVisibleCheckbox.isSelected()) {
            containerEP.setNavigable(containerVisibleCheckbox.isSelected());
        }
        if (containeeEP.isNavigable() != containeeVisibleCheckbox.isSelected()) {
            containeeEP.setNavigable(containeeVisibleCheckbox.isSelected());
        }
        
        this.dispose();
    }
    
