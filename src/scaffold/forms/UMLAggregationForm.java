    public void loadValues() {     
        compositeCheckBox.setSelected(assoc.isComposite());        
      
        CmAssocEnd containerEP = (link.getEndPoint1().getEPType() == UMLEndPoint.EPType.AGGREGATE)
                ? assoc.getEndPoint1() : assoc.getEndPoint2();        
        
        if (containerEP.getName() != null ) {
            containerRoleName.setText(containerEP.getName());
        }        
        
        if (containerEP.getMultiplicity() != null) {
            containerMultiplicity.setText(containerEP.getMultiplicity().getExpression());
        }                      
        
        CmAssocEnd containeeEP = (link.getEndPoint1().getEPType() != UMLEndPoint.EPType.AGGREGATE)
                ? assoc.getEndPoint1() : assoc.getEndPoint2();
        
        if (containeeEP.getName() != null ) {
            containeeRoleName.setText(containeeEP.getName());
        }        
        
        if (containeeEP.getMultiplicity() != null) {
            containeeMultiplicity.setText(containeeEP.getMultiplicity().getExpression());
        }             
        
        containerVisibleCheckbox.setSelected(containerEP.isNavigable());
        containeeVisibleCheckbox.setSelected(containeeEP.isNavigable());
    }
    
