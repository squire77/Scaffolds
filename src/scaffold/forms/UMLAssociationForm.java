    private void handleOK() {
        CmAssocEnd ep1 = relationship.getEndPoint1();
        
        if (ep1.getName() == null || !ep1.getName().equals(roleName1.getText())) {
            ep1.setName(roleName1.getText());
        }
        
        if (ep1.getMultiplicity() == null || !ep1.getMultiplicity().getExpression().equals(multiplicity1.getText())) {
            ep1.getMultiplicity().setExpression(multiplicity1.getText());
        }       
        
        CmAssocEnd ep2 = relationship.getEndPoint2();
        
        if (ep2.getName() == null || !ep2.getName().equals(roleName2.getText())) {
            ep2.setName(roleName2.getText());
        }
        
        if (ep2.getMultiplicity() == null || !ep2.getMultiplicity().getExpression().equals(multiplicity2.getText())) {
            ep2.getMultiplicity().setExpression(multiplicity2.getText());
        }
        
        if (ep1.isNavigable() != role1VisibleCheckbox.isSelected()) {
            ep1.setNavigable(role1VisibleCheckbox.isSelected());
        }
        if (ep2.isNavigable() != role2VisibleCheckbox.isSelected()) {
            ep2.setNavigable(role2VisibleCheckbox.isSelected());
        }
        
        this.dispose();
    }
    
