    public void loadValues() {
        CmAssocEnd ep1 = relationship.getEndPoint1();
        
        if (ep1.getName()!= null) {
            roleName1.setText(ep1.getName());
        }
        
        if (ep1.getMultiplicity() != null) {
            multiplicity1.setText(ep1.getMultiplicity().getExpression());
        }       
        
        CmAssocEnd ep2 = relationship.getEndPoint2();
        
        if (ep2.getName() != null) {
            roleName2.setText(ep2.getName());
        }
        
        if (ep2.getMultiplicity() != null) {
            multiplicity2.setText(ep2.getMultiplicity().getExpression());
        }        
                
        role1VisibleCheckbox.setSelected(ep1.isNavigable());
        role2VisibleCheckbox.setSelected(ep2.isNavigable());
    }
    
